package ch17.stream_element_processing;

public class JDK16CollectStudent {
    private String name;
    private String sex;
    private int score;

    public JDK16CollectStudent(String name, String sex, int score) {
        this.name = name;
        this.sex = sex;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public String getSex() {
        return sex;
    }

    public int getScore() {
        return score;
    }

}
